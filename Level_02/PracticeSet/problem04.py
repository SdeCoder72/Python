# Use comparison operator to find out whether a given variable a is greater than b or not. Take a and b as input

a = int(input("Enter number 1: "))
b = int(input("Enter number 2: "))

print(a, " > ", b , ": ", a>b)
print(a, " < ", b , ": ", a<b)
print(a, " >= ", b , ": ", a>=b)
print(a, " <= ", b , ": ", a<=b)
print(a, " == ", b , ": ", a==b)